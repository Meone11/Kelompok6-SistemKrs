    public void savetoFile(){
        
        try{
            
            FileOutputStream file = new FileOutputStream("regis.dat");
            ObjectOutputStream outputfile = new ObjectOutputStream(file);
            
            for (int i = 0; i < regis.size(); i++){
                outputfile.writeObject(regis.get(i));
            }
            
            outputfile.close();
            
            JOptionPane.showMessageDialog(null, "Successfully Register!!");
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        try{
            
            FileOutputStream file2 = new FileOutputStream("menu.dat");
            ObjectOutputStream outputfile2 = new ObjectOutputStream(file2);
            
            for (int i = 0; i < menu.size(); i++){
                outputfile2.writeObject(menu.get(i));
            }
            
            outputfile2.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
   
    }
    
    public void regisArrayList(){
        try{
            FileInputStream file = new FileInputStream("regis.dat");
            ObjectInputStream inputfile = new ObjectInputStream(file);
            
            boolean endoffile = false;
            
            while(!endoffile){
                try {
                    regis.add((Registrasi) inputfile.readObject());
                }catch (EOFException e){
                    endoffile = true;
                }catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputfile.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        try{
            FileInputStream file2 = new FileInputStream("menu.dat");
            ObjectInputStream inputfile2 = new ObjectInputStream(file2);
            
            boolean endoffile = false;
            
            while(!endoffile){
                try {
                    menu.add((Main_menu) inputfile2.readObject());
                }catch (EOFException e){
                    endoffile = true;
                }catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputfile2.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
    
    public boolean cekUsername(String Username){
        boolean cek = false;
        try{
            
            for (int i = 0; i < regis.size(); i++){
                if ( Username.equals(regis.get(i).getUserName()) ){
                    cek = true;
                    break;
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return cek;
    }
    
