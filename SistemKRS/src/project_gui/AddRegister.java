    private void signUpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_signUpMouseClicked
        // TODO add your handling code here:
        if (UserRegis.getText().isEmpty() || PassRegis.getText().isEmpty() ||
                namaRegis.getText().isEmpty() || Nim_r.getText().isEmpty() ||
                Email_r.getText().isEmpty() || TTL_r.getText().isEmpty() ||
                Telepon_r.getText().isEmpty() || Alamat_r.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please, Enter Your Username Or Password!!");
        }else{
            
            String Name = UserRegis.getText().trim();
            String Pass = PassRegis.getText().trim();
            String nama = namaRegis.getText().trim();
            String Nim = Nim_r.getText().trim();
            String email = Email_r.getText().trim();
            String TTL = TTL_r.getText().trim();
            String telepon = Telepon_r.getText().trim();
            String jenis_kelamin = jkArray[JK_r.getSelectedIndex()];
            String agama = agamaArray[Agama_r.getSelectedIndex()];
            String Alamat = Alamat_r.getText().trim();
            
            boolean cek = cekUsername(Name);
            
            if (cek){
                JOptionPane.showMessageDialog(null, Name + " Telah Digunakan, Silahkan Coba yang lain!");
            }else{
                Registrasi registrasi = new Registrasi(Name, Pass, nama, Nim, email, TTL,telepon, jenis_kelamin, agama, Alamat);
                regis.add(registrasi);            
            
                savetoFile();
            }           
            
        }
    }//GEN-LAST:event_signUpMouseClicked

