    public void regisArrayList(){
        try{
            FileInputStream file = new FileInputStream("regis.dat");
            ObjectInputStream inputfile = new ObjectInputStream(file);
            
            boolean endoffile = false;
            
            while(!endoffile){
                try {
                    regis.add((Registrasi) inputfile.readObject());
                }catch (EOFException e){
                    endoffile = true;
                }catch (Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            
            inputfile.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public int loginCheck(String Username, String Password){
        int index = 0;
        try{
            
            for (int i = 0; i < regis.size(); i++){
                if ( (Username.equals(cekName[i])) && Password.equals(cekPass[i]) ){
                    index = i;
                    break;
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return index;
    }
    
    public boolean boolloginCheck(String Username, String Password){
        boolean cek = false;
        try{
            
            for (int i = 0; i < regis.size(); i++){
                if ( (Username.equals(cekName[i])) && Password.equals(cekPass[i]) ){
                    cek = true;
                    break;
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        return cek;
    }

