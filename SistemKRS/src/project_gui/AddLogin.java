    private void LoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginButtonMouseClicked
        // TODO add your handling code here:
        if (UserLogin.getText().isEmpty() || PassLogin.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please, Enter Your UserName Or Password!!");

        } else{
            String Name = UserLogin.getText().trim();
            String Pass = PassLogin.getText().trim();

            int index = loginCheck(Name, Pass);
            boolean cek = boolloginCheck(Name, Pass);
            if ( cek ){
                MainMenu mainmenu = new MainMenu();
                 
                mainmenu.setVisible(true);
                mainmenu.namaDisplay(regis.get(index).getNama(), Name);
                mainmenu.Ambil(Name, Pass);
                                       
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "UserName Or Password Wrong");
            }
        }
    }//GEN-LAST:event_LoginButtonMouseClicked

